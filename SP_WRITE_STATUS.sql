END IF;

IF P_STATUS_TYPE = 'R' THEN
MERGE INTO STATUS S
USING (SELECT P_INFORM_ID INFORM_ID, P_STATUS STATUS FROM DUAL) TEMP ON (S.INFORM_ID = TEMP.INFORM_ID)
WHEN MATCHED THEN
